    private void BuscarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarConsultaActionPerformed
        // TODO add your handling code here:
        // 1.Obtener el id y validar 
        String idText = txtActualizar.getText();
        if (idText.isEmpty()){
            JOptionPane.showMessageDialog(this, "Id es obligatorio", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // 2. Ejecutamos la consult y listamos la tabla 
        try{
            int id = Integer.parseInt(idText);
            ResultSet consulta = crud.obtenerUsuarioPorID(id);
            DefaultTableModel modelo = (DefaultTableModel) TablaConsulta.getModel();
            modelo.setRowCount(0);
            
            if (consulta.next()){
                modelo.addRow(new Object[]{consulta.getInt("id"), consulta.getString("nombre"), consulta.getString("correo"), consulta.getString("contrasena") });
            }
            
        }
        catch(SQLException b){
            System.out.println("Error al llenar la tabla" + b.getMessage());
        }
    }//GEN-LAST:event_BuscarConsultaActionPerformed

