    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (txtActualizar.getText().isEmpty() || nombreNuevo.getText().isEmpty() || 
        correoNuevo.getText().isEmpty() || contrasenaNueva.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios", 
                                    "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
        
         int confirmacion = JOptionPane.showConfirmDialog(this, 
            "¿Está seguro que desea actualizar este usuario?", 
            "Confirmar Actualización", JOptionPane.YES_NO_OPTION);
        
        if (confirmacion != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            int id = Integer.parseInt(txtActualizar.getText());
        String nombre = nombreNuevo.getText();
        String correo = correoNuevo.getText();
        String contrasena = contrasenaNueva.getText();
        
        // Ejecutar actualización a través de UserCRUD
        boolean actualizado = crud.actualizarUsuario(id, nombre, correo, contrasena);
        
        if (actualizado) {
            JOptionPane.showMessageDialog(this, "Usuario actualizado correctamente", 
                                     "Éxito", JOptionPane.INFORMATION_MESSAGE);
            
            BuscarConsultaActionPerformed(null);
            
            nombreNuevo.setText("");
            correoNuevo.setText("");
            contrasenaNueva.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo actualizar el usuario", 
                                     "Error", JOptionPane.ERROR_MESSAGE);
        }   
      }catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "ID debe ser un número válido", 
                                   "Error", JOptionPane.ERROR_MESSAGE);
    } 
        
    }//GEN-LAST:event_jButton1ActionPerformed
 
